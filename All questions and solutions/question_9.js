// 9. Use trim() to remove any trailing whitespace at the beginning and the end of a string.

let str = "         hey, How are you            ";
let str1 = "where are u these days";
console.log(str + " " + str1);
console.log(str.trimStart() + " " + str1);
console.log(str.trimEnd() + " " + str1);
console.log(str.trim() + " " + str1);


